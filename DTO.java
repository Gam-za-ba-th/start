public class DTO {
String id;
String password;
String num;
String email;
public String getId() {
	return id;
}
public void setId(String id) {
	this.id = id;
}
public String getPassword() {
	return password;
}
public void setPassword(String password) {
	this.password = password;
}
public String getnum() {
	return num;
}
public void setnum(String job) {
	this.num = job;
}
public String getemail() {
	return email;
}
public void setemail(String email) {
	this.email = email;
}
}